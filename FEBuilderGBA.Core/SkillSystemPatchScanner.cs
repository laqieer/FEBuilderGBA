        public static bool IsClassSkillExtends(ROM rom)
        {
            if (rom == null || rom.Data == null) return false;
            uint start = 0xB00000;
            uint end = Math.Min(0xC00000u, (uint)rom.Data.Length);
            if (start >= end) return false;
            uint addr = U.Grep(rom.Data, s_ClassSkillExtendsPattern, start, end, 4);
            return addr != U.NOT_FOUND;
        }

        public static uint FindSkillPointerLocation(ROM rom, string type, uint extraSkip)
        {
            if (rom == null || rom.Data == null) return NOT_FOUND;

            uint start = 0xB00000;
            uint end = Math.Min(0xC00000u, (uint)rom.Data.Length);
            if (start >= end) return NOT_FOUND;

            foreach (var sig in s_Table)
            {
                if (sig.Type != type) continue;

                bool[] mask;
                bool useMask = MakeMaskData(sig.Pattern, out mask);
                uint found;
                if (useMask)
                {
                    found = GrepPatternMatch(rom.Data, sig.Pattern, mask, start, end, 4);
                }
                else
                {
                    found = U.Grep(rom.Data, sig.Pattern, start, end, 4);
                }
                if (found == U.NOT_FOUND) continue;

                uint a = (uint)(found + sig.Pattern.Length + sig.Skip);
                a = a + extraSkip;
                if (!U.isSafetyOffset(a, rom)) continue;

                uint p = rom.u32(a);
                if (!U.isSafetyPointer(p)) continue;

                if (type == "LEVELUP")
                {
                    uint baseAddr = U.toOffset(p);
                    if (!U.isSafetyOffset(baseAddr + 11, rom)) continue;

                    uint data0th = rom.u32(baseAddr);
                    uint data1st = rom.u32(baseAddr + 4);
                    uint data2nd = rom.u32(baseAddr + 8);

                    if (!U.isSafetyPointerOrNull(data0th)) continue;
                    if (!U.isSafetyPointerOrNull(data1st)) continue;
                    if (!U.isSafetyPointerOrNull(data2nd)) continue;
                }

                return a;
            }
            return NOT_FOUND;
        }

        static bool MakeMaskData(byte[] grepdata, out bool[] outMask)
        {
            var maskData = new List<bool>();
            bool useMask = false;
            int i;
            for (i = 0; i < grepdata.Length - 4; i++)
            {
                if (grepdata[i] == 0xFF && grepdata[i + 1] == 0xFF
                    && grepdata[i + 2] == 0xFF && grepdata[i + 3] == 0xFF)
                {
                    maskData.Add(true);
                    maskData.Add(true);
                    maskData.Add(true);
                    maskData.Add(true);
                    useMask = true;
                    i += 3;
                }
                else
                {
                    maskData.Add(false);
                }
            }
            for (; i < grepdata.Length; i++)
            {
                maskData.Add(false);
            }
            outMask = maskData.ToArray();
            return useMask;
        }

        static uint GrepPatternMatch(byte[] romData, byte[] pattern, bool[] mask, uint start, uint end, uint blockSize)
        {
            if (romData == null || pattern == null || pattern.Length == 0) return U.NOT_FOUND;
            if (end == 0 || end > (uint)romData.Length) end = (uint)romData.Length;
            if (start + pattern.Length > end) return U.NOT_FOUND;

            uint limit = end - (uint)pattern.Length;
            for (uint pos = start; pos <= limit; pos += blockSize)
            {
                bool match = true;
                for (int j = 0; j < pattern.Length; j++)
                {
                    if (!mask[j] && romData[pos + j] != pattern[j])
                    {
                        match = false;
                        break;
                    }
                }
                if (match) return pos;
            }
            return U.NOT_FOUND;
        }
