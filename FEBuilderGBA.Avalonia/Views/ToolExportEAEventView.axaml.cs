        async void ExportMainTable_Click(object? sender, RoutedEventArgs e)
        {
            var rom = CoreState.ROM;
            if (rom == null) return;

            try
            {
                string? path = await PickSaveFile("MainTables.event");
                if (path == null) return;

                var sb = new StringBuilder();
                sb.AppendLine("// Main Table export");
                sb.AppendLine($"// ROM: {rom.RomInfo.VersionToFilename}");
                sb.AppendLine($"// Generated by FEBuilderGBA Avalonia");
                sb.AppendLine();

                // Export core tables using ExportFunction for key pointers
                var ef = new ExportFunction();

                // Unit table
                uint unitBase = rom.p32(rom.RomInfo.unit_pointer);
                if (U.isSafetyOffset(unitBase))
                    ef.Add("UnitTable", U.toPointer(unitBase));

                // Class table
                uint classBase = rom.p32(rom.RomInfo.class_pointer);
                if (U.isSafetyOffset(classBase))
                    ef.Add("ClassTable", U.toPointer(classBase));

                // Item table
                uint itemBase = rom.p32(rom.RomInfo.item_pointer);
                if (U.isSafetyOffset(itemBase))
                    ef.Add("ItemTable", U.toPointer(itemBase));

                ef.ExportEA(sb);
                sb.AppendLine();

                // Dump table data using StructExportCore for entry counts
                string[] tableNames = { "units", "classes", "items" };
                uint[] tableAddrs = { unitBase, classBase, itemBase };
                for (int t = 0; t < tableNames.Length; t++)
                {
                    uint baseAddr = tableAddrs[t];
                    if (!U.isSafetyOffset(baseAddr)) continue;

                    var tableDef = StructExportCore.GetTable(tableNames[t]);
                    if (tableDef == null) continue;

                    uint entryCount = tableDef.GetEntryCount(rom);
                    uint dataSize = tableDef.GetDataSize(rom);
                    uint totalSize = entryCount * dataSize;

                    sb.AppendLine($"// {tableNames[t]} data ({entryCount} entries, {dataSize} bytes each)");
                    sb.AppendLine($"ORG {U.To0xHexString(baseAddr)}");
                    sb.AppendLine($"BYTE {FormatBytesDollar(rom.Data, baseAddr, totalSize)}");
                    sb.AppendLine();
                }

                File.WriteAllText(path, sb.ToString());
                _vm.StatusText = $"Exported main tables to: {Path.GetFileName(path)}";
            }
            catch (Exception ex)
            {
                Log.Error("ToolExportEAEventView.ExportMainTable", ex.ToString());
                _vm.StatusText = $"Export failed: {ex.Message}";
            }
        }

        async void ExportUndoData_Click(object? sender, RoutedEventArgs e)
        {
            var rom = CoreState.ROM;
            var undo = CoreState.Undo;
            if (rom == null || undo == null) return;

            try
            {
                string? path = await PickSaveFile("UndoBuffer.event");
                if (path == null) return;

                var checkDupMap = new Dictionary<string, bool>();
                var revLines = new List<string>();

                // Iterate undo buffer from newest to oldest to avoid duplicates
                for (int i = undo.UndoBuffer.Count - 1; i >= 0; i--)
                {
                    Undo.UndoData undodata = undo.UndoBuffer[i];
                    bool isOutput = false;

                    for (int n = undodata.list.Count - 1; n >= 0; n--)
                    {
                        Undo.UndoPostion ud = undodata.list[n];
                        string key = ud.addr + "@" + ud.data.Length;
                        if (checkDupMap.ContainsKey(key))
                            continue;
                        checkDupMap[key] = true;

                        if (ud.data.Length >= 1024 * 1024 * 2)
                        {
                            revLines.Add("//SKIP THIS DATA 2M OVER");
                            continue;
                        }
                        revLines.Add("BYTE " + FormatBytesDollar(rom.Data, ud.addr, (uint)ud.data.Length) + ";");
                        revLines.Add("ORG $" + U.ToHexString(ud.addr));
                        isOutput = true;
                    }
                    if (isOutput)
                    {
                        revLines.Add("//" + undodata.name + undodata.time);
                    }
                }

                var sb = new StringBuilder();
                for (int i = revLines.Count - 1; i >= 0; i--)
                    sb.AppendLine(revLines[i]);

                File.WriteAllText(path, sb.ToString());
                _vm.StatusText = $"Exported undo buffer to: {Path.GetFileName(path)}";
            }
            catch (Exception ex)
            {
                Log.Error("ToolExportEAEventView.ExportUndoData", ex.ToString());
                _vm.StatusText = $"Export failed: {ex.Message}";
            }
        }

