                sb.AppendLine($"// Generated by FEBuilderGBA Avalonia");
                sb.AppendLine();

                var ef = new ExportFunction();
                ef.Add($"Map{mapIndex}_WorldMapEvent", U.toPointer(addr));
                ef.ExportEA(sb);

                File.WriteAllText(path, sb.ToString());
