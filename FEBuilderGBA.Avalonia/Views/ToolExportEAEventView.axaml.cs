        async System.Threading.Tasks.Task<string?> PickSaveFile(string suggestedName)
        {
            var file = await this.StorageProvider.SaveFilePickerAsync(new FilePickerSaveOptions
            {
                Title = "Export EA Event",
                SuggestedFileName = suggestedName,
                FileTypeChoices = new[] { EaFileType, AllFileType },
            });
            return file?.TryGetLocalPath();
        }

        /// <summary>Format ROM bytes as EA BYTE command with $ hex prefix.</summary>
        static string FormatBytesDollar(byte[] data, uint addr, uint length)
        {
            var sb = new StringBuilder();
            for (uint i = 0; i < length && (addr + i) < (uint)data.Length; i++)
            {
                if (i > 0) sb.Append(' ');
                sb.Append('$');
                sb.Append(data[addr + i].ToString("X2"));
            }
            return sb.ToString();
        }

        async void ExportEvents_Click(object? sender, RoutedEventArgs e)
        {
            var rom = CoreState.ROM;
            if (rom == null) return;

            int mapIndex = _vm.SelectedMapIndex;
            if (mapIndex < 0)
            {
                _vm.StatusText = "Please select a map first.";
                return;
            }

            try
            {
                // Get event address for the selected map using MapEventUnitCore
                uint eventAddr = MapEventUnitCore.GetEventAddrForMap(rom, (uint)mapIndex);
                if (!U.isSafetyOffset(eventAddr) || eventAddr == U.NOT_FOUND)
                {
                    _vm.StatusText = "Event address not found for this map.";
                    return;
                }

                string suggestedName = $"Map{mapIndex:D2}_Event_0x{eventAddr:X08}.event";
                string? path = await PickSaveFile(suggestedName);
                if (path == null) return;

                var sb = new StringBuilder();
                sb.AppendLine($"// Event export for Map {mapIndex} (0x{eventAddr:X08})");
                sb.AppendLine($"// Generated by FEBuilderGBA Avalonia");
                sb.AppendLine();

                // Export raw event data as ORG + BYTE blocks
                var ef = new ExportFunction();
                ef.Add($"Map{mapIndex}_Event", U.toPointer(eventAddr));
                ef.ExportEA(sb);

                // Write event data bytes
                uint dataLen = 0;
                for (uint i = 0; i < 0x10000 && (eventAddr + i) < (uint)rom.Data.Length; i += 4)
                {
                    uint val = rom.u32(eventAddr + i);
                    if (val == 0 && i > 0) { dataLen = i + 4; break; }
                }
                if (dataLen == 0) dataLen = 256; // fallback
                if (dataLen > 0)
                {
                    sb.AppendLine($"ORG {U.To0xHexString(eventAddr)}");
                    sb.AppendLine($"BYTE {FormatBytesDollar(rom.Data, eventAddr, dataLen)}");
                }

                File.WriteAllText(path, sb.ToString());
                _vm.StatusText = $"Exported events to: {Path.GetFileName(path)}";
            }
            catch (Exception ex)
            {
                Log.Error("ToolExportEAEventView.ExportEvents", ex.ToString());
                _vm.StatusText = $"Export failed: {ex.Message}";
            }
        }

        async void ExportWMapEvents_Click(object? sender, RoutedEventArgs e)
        {
            var rom = CoreState.ROM;
            if (rom == null) return;

            int mapIndex = _vm.SelectedMapIndex;
            if (mapIndex < 0)
            {
                _vm.StatusText = "Please select a map first.";
                return;
            }

            try
            {
                // Get world map event address from the stageclear pointer table
                uint wmapEventPtr = rom.RomInfo.worldmap_event_on_stageclear_pointer;
                if (wmapEventPtr == 0)
                {
                    _vm.StatusText = "World map events not available for this ROM version.";
                    return;
                }

                uint tableBase = rom.p32(wmapEventPtr);
                if (!U.isSafetyOffset(tableBase))
                {
                    _vm.StatusText = "World map event table not found.";
                    return;
                }

                uint addr = rom.p32(tableBase + (uint)(mapIndex * 4));
                if (!U.isSafetyOffset(addr) || addr == U.NOT_FOUND)
                {
                    _vm.StatusText = "No world map event found for this map.";
                    return;
                }

                string suggestedName = $"Map{mapIndex:D2}_WMap_0x{addr:X08}.event";
                string? path = await PickSaveFile(suggestedName);
                if (path == null) return;

                var sb = new StringBuilder();
